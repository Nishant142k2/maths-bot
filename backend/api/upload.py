# backend/app/api/upload.py

from fastapi import APIRouter, UploadFile, File, Form, HTTPException, BackgroundTasks
from typing import Optional
import uuid
from core.supabase_client import supabase
from services.ocr_asr_service import ocr_asr_service
from services.file_service import file_service
from services.memory_service import memory_service
from utils.validators import validators
from utils.logger import logger
from utils.exceptions import OCRException, ASRException, ValidationException
from pydantic import BaseModel

router = APIRouter(prefix="/upload", tags=["Upload"])

class UploadResponse(BaseModel):
    session_id: str
    status: str  # "processing", "completed", "error"
    media_url: Optional[str] = None
    media_type: str  # "image", "audio", "text"
    extracted_text: Optional[str] = None
    confidence: float = 0.0
    needs_review: bool = True
    error: Optional[str] = None

class TextUploadRequest(BaseModel):
    text: str

async def process_upload_background(
    session_id: str,
    file_bytes: bytes,
    filename: str,
    content_type: str
):
    """Background task for processing uploads asynchronously"""
    try:
        logger.info(f"[Background] Processing upload for session: {session_id}")
        
        # Upload file to storage
        upload_result = await file_service.upload_file(
            file_bytes=file_bytes,
            filename=filename,
            content_type=content_type,
            session_id=session_id
        )
        
        if not upload_result["success"]:
            raise Exception(upload_result["error"])
        
        media_url = upload_result["url"]
        media_type = upload_result["media_type"]
        
        # Process based on media type
        if media_type == "image":
            ocr_result = await ocr_asr_service.process_image(file_bytes, filename)
            extracted_text = ocr_result["extracted_text"]
            confidence = ocr_result["confidence"]
            needs_review = ocr_result["needs_review"]
            
            if not ocr_result["success"]:
                raise OCRException(ocr_result["error"])
                
        elif media_type == "audio":
            asr_result = await ocr_asr_service.process_audio(file_bytes, filename)
            extracted_text = asr_result["extracted_text"]
            confidence = asr_result["confidence"]
            needs_review = asr_result["needs_review"]
            
            if not asr_result["success"]:
                raise ASRException(asr_result["error"])
        else:
            extracted_text = ""
            confidence = 0.0
            needs_review = True
        
        # Create session in Supabase
        session_data = {
            "id": session_id,
            "user_input": extracted_text,
            "media_url": media_url,
            "media_type": media_type,
            "ocr_text": extracted_text,
            "hitl_status": "pending" if needs_review else "approved",
            "created_at": None,  # Auto-generated by DB
            "updated_at": None
        }
        
        supabase.table("sessions").insert(session_data).execute()
        
        # Cache session
        memory_service.cache_session(session_id, {
            "session_id": session_id,
            "extracted_text": extracted_text,
            "media_url": media_url,
            "media_type": media_type,
            "confidence": confidence,
            "needs_review": needs_review,
            "status": "completed"
        })
        
        logger.info(f"[Background] Upload processing complete for session: {session_id}")
        
    except Exception as e:
        logger.error(f"[Background] Upload processing failed: {str(e)}")
        # Update session with error
        supabase.table("sessions").update({
            "hitl_status": "error",
            "hitl_feedback": str(e)
        }).eq("id", session_id).execute()

@router.post("/image", response_model=UploadResponse)
async def upload_image(
    file: UploadFile = File(...),
    background_tasks: BackgroundTasks = None
):
    """
    Upload image for OCR processing.
    Returns session_id immediately, processing happens in background.
    """
    try:
        # Validate file type
        is_valid, error = validators.validate_file_type(file.content_type)
        if not is_valid:
            raise ValidationException(error)
        
        # Generate session ID
        session_id = str(uuid.uuid4())
        
        # Read file bytes
        file_bytes = await file.read()
        
        # Start background processing
        if background_tasks:
            background_tasks.add_task(
                process_upload_background,
                session_id,
                file_bytes,
                file.filename or "image.jpg",
                file.content_type
            )
        
        # Return immediate response (processing in background)
        return UploadResponse(
            session_id=session_id,
            status="processing",
            media_url=None,
            media_type="image",
            extracted_text=None,
            confidence=0.0,
            needs_review=True,
            error=None
        )
        
    except ValidationException as e:
        raise HTTPException(status_code=400, detail=e.message)
    except Exception as e:
        logger.error(f"Image upload error: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))

