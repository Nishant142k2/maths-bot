@router.post("/text", response_model=UploadResponse)
async def upload_text(request: TextUploadRequest):
    """
    Submit text input directly (no OCR/ASR needed).
    """
    try:
        # Validate text
        is_valid, error = validators.validate_math_text(request.text)
        if not is_valid:
            raise ValidationException(error)
        
        # Generate session ID
        session_id = str(uuid.uuid4())
        
        # Create session in Supabase (text doesn't need background processing)
        session_data = {
            "id": session_id,
            "user_input": request.text,
            "media_url": None,
            "media_type": "text",
            "ocr_text": request.text,
            "hitl_status": "approved",  # Text input doesn't need OCR review
            "created_at": None,
            "updated_at": None
        }
        
        supabase.table("sessions").insert(session_data).execute()
        
        # Cache session
        memory_service.cache_session(session_id, {
            "session_id": session_id,
            "extracted_text": request.text,
            "media_url": None,
            "media_type": "text",
            "confidence": 1.0,
            "needs_review": False,
            "status": "completed"
        })
        
        return UploadResponse(
            session_id=session_id,
            status="completed",
            media_url=None,
            media_type="text",
            extracted_text=request.text,
            confidence=1.0,
            needs_review=False,
            error=None
        )
        
    except ValidationException as e:
        raise HTTPException(status_code=400, detail=e.message)
    except Exception as e:
        logger.error(f"Text upload error: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))

@router.get("/{session_id}", response_model=UploadResponse)
async def get_upload_status(session_id: str):
    """
    Check status of upload processing (for polling).
    """
    try:
        # Validate session ID
        is_valid, error = validators.validate_session_id(session_id)
        if not is_valid:
            raise ValidationException(error)
        
        # Check cache first
        cached = memory_service.get_cached_session(session_id)
        if cached:
            return UploadResponse(
                session_id=session_id,
                status=cached.get("status", "processing"),
                media_url=cached.get("media_url"),
                media_type=cached.get("media_type", "text"),
                extracted_text=cached.get("extracted_text"),
                confidence=cached.get("confidence", 0.0),
                needs_review=cached.get("needs_review", True),
                error=None
            )
        
        # Query Supabase
        response = supabase.table("sessions").select("*").eq("id", session_id).execute()
        
        if not response.data or len(response.data) == 0:
            raise HTTPException(status_code=404, detail="Session not found")
        
        row = response.data[0]
        
        return UploadResponse(
            session_id=session_id,
            status="completed" if row.get("ocr_text") else "processing",
            media_url=row.get("media_url"),
            media_type=row.get("media_type", "text"),
            extracted_text=row.get("ocr_text") or row.get("user_input"),
            confidence=1.0 if row.get("media_type") == "text" else 0.7,
            needs_review=row.get("hitl_status") == "pending",
            error=row.get("hitl_feedback")
        )
        
    except ValidationException as e:
        raise HTTPException(status_code=400, detail=e.message)
    except HTTPException:
        raise
    except Exception as e:
        logger.error(f"Upload status check error: {str(e)}")
        raise HTTPException(status_code=500, detail=str(e))